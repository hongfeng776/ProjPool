    def generate_wind_report(self, input_params: Dict, results: Dict) -> str:
        building_name = input_params.get("building_name", "未命名建筑")
        region = input_params.get("region", "北京")
        height = input_params.get("height", 0)
        shape_factor = input_params.get("shape_factor", 1.3)

        w0 = results.get('基本风压 w0', {}).get('value', 0)
        mu_z = results.get('高度变化系数 μz', {}).get('value', 0)
        beta_z = results.get('风振系数 βz', {}).get('value', 0)
        mu_s = results.get('体型系数 μs', {}).get('value', 0)
        wk = results.get('风荷载标准值 wk', {}).get('value', 0)

        report = []
        report.append("=" * 70)
        report.append("")
        report.append("             风 荷 载 计 算 书")
        report.append("")
        report.append("=" * 70)
        report.append("")
        report.append(f"工程名称：{building_name}")
        report.append(f"计算类型：风荷载计算")
        report.append(f"计算日期：{datetime.datetime.now().strftime('%Y年%m月%d日')}")
        report.append(f"规范依据：《建筑结构荷载规范》GB50009-2012")
        report.append("")
        report.append("-" * 70)
        report.append("")
        report.append("一、输入参数")
        report.append("")
        report.append(f"  1. 所在地区：{region}")
        report.append(f"  2. 建筑高度：H = {height:.2f} m")
        report.append(f"  3. 体型系数：μs = {mu_s:.2f}")
        report.append("  4. 地貌类型：B类（郊区、丘陵、中小城镇）")
        report.append("")
        report.append("-" * 70)
        report.append("")
        report.append("二、风荷载计算")
        report.append("")
        report.append("  计算公式：")
        report.append("  w_k = β_z × μ_s × μ_z × w_0")
        report.append("")
        report.append("  【1】基本风压 w_0")
        report.append(f"      根据 GB50009-2012，{region} 地区基本风压：")
        report.append(f"      w_0 = {w0:.2f} kN/m²")
