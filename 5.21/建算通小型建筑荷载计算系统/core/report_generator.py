    def generate_snow_report(self, input_params: Dict, results: Dict) -> str:
        building_name = input_params.get("building_name", "未命名建筑")
        region = input_params.get("region", "北京")
        roof_type = input_params.get("roof_type", "平屋面(i≤5°)")

        s0 = results.get('基本雪压 s0', {}).get('value', 0)
        mu_r = results.get('积雪分布系数 μr', {}).get('value', 0)
        sk = results.get('雪荷载标准值 sk', {}).get('value', 0)
        snow_district = results.get('雪压分区', {}).get('value', '-')

        report = []
        report.append("=" * 70)
        report.append("")
        report.append("             雪 荷 载 计 算 书")
        report.append("")
        report.append("=" * 70)
        report.append("")
        report.append(f"工程名称：{building_name}")
        report.append(f"计算类型：雪荷载计算")
        report.append(f"计算日期：{datetime.datetime.now().strftime('%Y年%m月%d日')}")
        report.append(f"规范依据：《建筑结构荷载规范》GB50009-2012")
        report.append("")
        report.append("-" * 70)
        report.append("")
        report.append("一、输入参数")
        report.append("")
        report.append(f"  1. 所在地区：{region}")
        report.append(f"  2. 屋面类型：{roof_type}")
        report.append(f"  3. 雪压分区：{snow_district} 区")
        report.append("")
        report.append("-" * 70)
        report.append("")
        report.append("二、雪荷载计算")
        report.append("")
        report.append("  计算公式：")
        report.append("  s_k = μ_r × s_0")
        report.append("")
        report.append("  【1】基本雪压 s_0")
        report.append(f"      根据 GB50009-2012 附录E，{region} 地区基本雪压：")
        report.append(f"      s_0 = {s0:.2f} kN/m²")
        report.append("")
        report.append("  【2】屋面积雪分布系数 μ_r")
        report.append(f"      屋面类型：{roof_type}")
        report.append(f"      查表 GB50009-2012 表6.2.1：")
        report.append(f"      μ_r = {mu_r:.2f}")
        report.append("")
        report.append("  【3】雪荷载标准值 s_k")
        report.append(f"      s_k = μ_r × s_0")
        report.append(f"          = {mu_r:.2f} × {s0:.2f}")
        report.append(f"          = {sk:.4f} kN/m²")
        report.append("")
        report.append("-" * 70)
        report.append("")
        report.append("三、计算结果汇总表")
        report.append("")
        report.append("  ┌──────────────────────┬──────────────┬──────────┐")
        report.append("  │      计算项目        │     数值     │   单位   │")
        report.append("  ├──────────────────────┼──────────────┼──────────┤")
        report.append(f"  │ 基本雪压 s₀          │  {s0:>10.2f}  │  kN/m²  │")
        report.append(f"  │ 积雪分布系数 μ_r     │  {mu_r:>10.2f}  │  -      │")
        report.append(f"  │ 雪荷载标准值 s_k     │  {sk:>10.4f}  │  kN/m²  │")
        report.append("  └──────────────────────┴──────────────┴──────────┘")
        report.append("")
        if s0 == 0:
            report.append("  注：该地区基本雪压为 0，无需考虑雪荷载作用。")
            report.append("")
        report.append("=" * 70)
        report.append("")
        report.append(f"计算人：建算通系统  v1.0")
        report.append(f"校对人：自动生成")
        report.append("")
        report.append("=" * 70)

        return "\n".join(report)

    def report_to_text(self, report_content: str, filepath: str):
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(report_content)
        return filepath
