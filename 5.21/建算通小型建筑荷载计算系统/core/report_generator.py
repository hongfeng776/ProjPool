        report.append(f"      建筑高度 H = {height:.2f} m，B类地貌")
        report.append(f"      查表 GB50009-2012 表8.2.1：")
        report.append(f"      μ_z = {mu_z:.4f}")
        report.append("")
        report.append("  【4】风振系数 β_z")
        if height <= 30:
            report.append(f"      高度 H ≤ 30m，取 β_z = {beta_z:.1f}")
        else:
            report.append(f"      高度 H > 30m，考虑风振：β_z = {beta_z:.4f}")
        report.append("")
        report.append("  【5】风荷载标准值 w_k")
        report.append(f"      w_k = β_z × μ_s × μ_z × w_0")
        report.append(f"          = {beta_z:.4f} × {mu_s:.2f} × {mu_z:.4f} × {w0:.2f}")
        report.append(f"          = {wk:.4f} kN/m²")
