        report.append("  ┌──────────────────────┬──────────────┬──────────┐")
        report.append("  │      计算项目        │     数值     │   单位   │")
        report.append("  ├──────────────────────┼──────────────┼──────────┤")
        report.append(f"  │ 基本风压 w₀          │  {w0:>10.2f}  │  kN/m²  │")
        report.append(f"  │ 体型系数 μ_s         │  {mu_s:>10.2f}  │  -      │")
        report.append(f"  │ 高度变化系数 μ_z     │  {mu_z:>10.4f}  │  -      │")
        report.append(f"  │ 风振系数 β_z         │  {beta_z:>10.4f}  │  -      │")
